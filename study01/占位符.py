name = "jaychou"
age = 18
#   比如18888.8888 长度是10
salary = 18888.8888
print(f"我叫{name} 我今年{age}岁 我的工资是:{salary}")


# %8.1  意思是长度最高8 小数点后最多1位
# %d 是整数   %f 是浮点
print("我叫%s 我今年%d岁 我的工资是:%8.1f" % (name, age, salary))

